# São operadores utilizados para comparar dois valores.

saldo = 450
saque = 2000

print(saldo == saque) # >> False
print(saldo != saque) # >> True
print(saldo > saque)  # >> False
print(saldo >= saque) # >> False
print(saldo < saque) # >> True
print(saldo <= saque) # >> True

'''
São os operadores
== igual, != diferente, > maior que, >= maior ou igual, < menor que, <= menor ou igual.
'''

